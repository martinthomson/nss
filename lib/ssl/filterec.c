/* A bitvector of the enabled groups. */
static PRUint32 enabledGroups = 0;
/* Check that the supported groups bits will fit into this bitvector. */
PR_STATIC_ASSERT(SSL_NAMED_GROUP_COUNT < (sizeof(PRUint32) * 8));
/* Only run this filtering once. */
static PRCallOnceType setupGroupMaskOnce;

/* This imports a public key from the table able for each of the supported EC
 * groups.  If that works, we assume that the token supports that group.  Since
 * this is relatively expensive, this is only done for the first socket that is
 * used.  That means that if tokens are added or removed, then this will not
 * pick up any changes.
 *
 * Groups of other types are simply enabled.
 */
static PRStatus
ssl_SetupGroupMask(void *arg)
{
    sslSocket *ss = (sslSocket *)arg;
    unsigned int i;
    PORTCheapArenaPool arena;

    PORT_InitCheapArena(&arena, DER_DEFAULT_CHUNKSIZE);

    enabledGroups = 0;
    for (i = 0; i < SSL_NAMED_GROUP_COUNT; ++i) {
        PK11SlotInfo *slot;
        SECKEYPublicKey pubKey;
        CK_OBJECT_HANDLE obj;
        SECStatus rv;
        const namedGroupDef *group = &ssl_named_groups[i];

        PORT_Assert(group->name == ssl_test_public_keys[i].name);
        if (group->type != group_type_ec) {
            enabledGroups |= 1U << i;
            continue;
        }

        pubKey.keyType = ecKey;
        pubKey.u.ec.publicValue.data = /* const cast */
            (unsigned char *)ssl_test_public_keys[i].data;
        pubKey.u.ec.publicValue.len = ssl_test_public_keys[i].len;
        pubKey.u.ec.DEREncodedParams.data = NULL;
        rv = ssl_NamedGroup2ECParams(&arena.arena, group,
                                     &pubKey.u.ec.DEREncodedParams);
        PORT_Assert(rv == SECSuccess);
        if (rv != SECSuccess) {
            continue;
        }

        slot = PK11_GetBestSlotWithAttributes(CKM_ECDH1_DERIVE, 0, group->bits,
                                              ss->pkcs11PinArg);
        if (!slot) {
            continue;
        }

        obj = PK11_ImportPublicKey(slot, &pubKey, PR_FALSE);
        if (obj) {
            /* This group is apparently supported. */
            enabledGroups |= 1U << i;
            PK11_DestroyObject(slot, obj);
        }
        PK11_FreeSlot(slot);
    }

    SSL_TRC(5, ("%d: SSL[%d]: enabled group mask %x",
                SSL_GETPID(), ss->fd, enabledGroups));
    PORT_DestroyCheapArena(&arena);
    return PR_SUCCESS;
}

void
ssl_FilterSupportedGroups(sslSocket *ss)
{
    unsigned int i;
    PRStatus rv;

    rv = PR_CallOnceWithArg(&setupGroupMaskOnce,
                            ssl_SetupGroupMask,
                            (void *)ss);
    PORT_Assert(rv == PR_SUCCESS);

    for (i = 0; i < SSL_NAMED_GROUP_COUNT; ++i) {
        PRUint32 policy;
        SECStatus rv;
        unsigned int index;
        const namedGroupDef *group = ss->namedGroupPreferences[i];
        if (!group) {
            continue;
        }

        rv = NSS_GetAlgorithmPolicy(group->oidTag, &policy);
        if (rv == SECSuccess && !(policy & NSS_USE_ALG_IN_SSL_KX)) {
            ss->namedGroupPreferences[i] = NULL;
            continue;
        }

        index = group - ssl_named_groups;
        PORT_Assert(index < SSL_NAMED_GROUP_COUNT);
        if ((enabledGroups & (1U << index)) == 0) {
            SSL_TRC(5, ("%d: SSL[%d]: disable absent named group %d",
                        SSL_GETPID(), ss->fd, group->name));
            ss->namedGroupPreferences[i] = NULL;
        }
    }
}
